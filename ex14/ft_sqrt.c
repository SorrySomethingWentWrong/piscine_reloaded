int		ft_sqrt(int nb)
{
	int		index;

	if (nb < 1 && !(nb << 15))
		return (0);
	index = 1;
	while (!(index * index >= nb))
	{
		index++;
	}
	if (nb)
	{
		if (index * index == nb)
			return (index);
	}
	return (0);
}
